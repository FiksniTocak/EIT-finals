USE Polovni_Automobili;


CREATE TABLE Proizvodjac(
    ProizvodjacID INT PRIMARY KEY,
    Naziv VARCHAR(50)
    );
CREATE TABLE Gorivo(
    GorivoID INT PRIMARY KEY,
    Naziv VARCHAR(50),
    Opis TEXT
    );
CREATE TABLE Boja(
    BojaID  INT PRIMARY KEY,
    Naziv VARCHAR(50),
    Opis TEXT
    );

GO

CREATE TABLE Model(
    ModelID INT PRIMARY KEY,
    Naziv VARCHAR(50),
    ProizvodjacID INT,
    CONSTRAINT fk_Proiz_Model FOREIGN KEY (ProizvodjacID) REFERENCES proizvodjac(proizvodjacID)
    );
GO

CREATE TABLE Vozilo(
    VoziloID INT PRIMARY KEY,
    Registracija VARCHAR(50),
    GodinaProizvodnje INT,
    PredjenoKM INT,
    ModelID INT, 
    BojaID INT, 
    GorivoID INT, 
    CONSTRAINT fk_Vozilo_Model FOREIGN KEY (ModelID) REFERENCES model(ModelID), 
    CONSTRAINT fk_Vozilo_Boja FOREIGN KEY (BojaID) REFERENCES boja(BojaID),
    CONSTRAINT fk_Vozilo_Gorivo FOREIGN KEY (GorivoID) REFERENCES gorivo(GorivoID)
    );

